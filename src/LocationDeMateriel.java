
class LocationDeMateriel {
	private String nom;
	   private double prix;
	   public LocationDeMateriel(String nom, double prix) {
	       this.nom = nom;
	       this.prix = prix;
	   }
	   public String getNom() {
	       return nom;
	   }
	   public double getPrix() {
	       return prix;
	   }
	}


